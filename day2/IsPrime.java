package week1.day2;

public class IsPrime {
	
	    public static void main(String[] args) {
	        int n=20;
	        for(int i=2;i<=n;){
	        if(n%i==0)
	        {
	            System.out.println("This is not a prime number");
	            break;
	        }
	            else{
	        System.out.println("This is a prime number");
	        break;
	    }
	}
	}
	}


